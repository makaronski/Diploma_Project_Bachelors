Comedian Will Ferrell was in attendance at Anfield on Saturday afternoon and would’ve expected to witness Liverpool making a mockery of Swansea City’s porous rearguard.
<p>
But it was Paul Clement who enjoyed the last laugh; his side securing their first-ever league victory at the storied stadium on their 16th attempt.
