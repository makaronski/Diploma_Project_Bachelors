           	 <h1 style="margin: 30px 0px; font-family: BentonSansBold, Helvetica, Arial, sans-serif; font-weight: normal; line-height: 44px; font-style: normal; font-variant: normal; font-stretch: normal; font-size: 36px; letter-spacing: -1px; font-feature-settings: 'kern' 1, 'liga' 1, 'case' 1; transition: color 0.2s; color: rgb(40, 42, 47); orphans: 2; text-align: start; text-indent: 0px; text-transform: none; white-space: normal; widows: 2; word-spacing: 0px; -webkit-text-stroke-width: 0px;">
			 A brief history of Liverpool</h1>
				<div class="body full" style="color: rgb(40, 42, 47); font-family: BentonSans, Helvetica, Arial, sans-serif; font-size: medium; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal; letter-spacing: normal; orphans: 2; text-align: start; text-indent: 0px; text-transform: none; white-space: normal; widows: 2; word-spacing: 0px; -webkit-text-stroke-width: 0px;">
					<div class="above-fold">
						<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;"><photo1></photo1>
						</p>
						<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;">
						<b style="font-family: BentonSansBold; font-weight: normal;">
						Formed:</b><span class="Apple-converted-space">&nbsp;</span>1892
						<br />
						<b style="font-family: BentonSansBold; font-weight: normal;">
						Admitted to Football League:</b><span class="Apple-converted-space">&nbsp;</span>1893<br />
						<b style="font-family: BentonSansBold; font-weight: normal;">
						European Cup/Champions League:</b><span class="Apple-converted-space">&nbsp;</span>1976-77, 
						1977-78, 1980-81, 1983-84, 2004-05<br />
						<b style="font-family: BentonSansBold; font-weight: normal;">
						UEFA Cup:</b><span class="Apple-converted-space">&nbsp;</span>1972-73,&nbsp;1975-76,&nbsp;2000-01<br />
						<b style="font-family: BentonSansBold; font-weight: normal;">
						UEFA Super Cup:</b><span class="Apple-converted-space">&nbsp;</span>1977,&nbsp;2001,&nbsp;2005<br />
						<b style="font-family: BentonSansBold; font-weight: normal;">
						First Division:</b><span class="Apple-converted-space">&nbsp;</span>18<br />
						<b style="font-family: BentonSansBold; font-weight: normal;">
						Second Division:</b><span class="Apple-converted-space">&nbsp;</span>4<br />
						<b style="font-family: BentonSansBold; font-weight: normal;">
						FA Cup:</b><span class="Apple-converted-space">&nbsp;</span>7<br />
						<b style="font-family: BentonSansBold; font-weight: normal;">
						League Cup:</b><span class="Apple-converted-space">&nbsp;</span>7</p>
						The story of Liverpool is a unique fusion of triumph and 
						tragedy. They are English football's most successful 
						club with 40 trophies, yet towards the end of their rule 
						of English football came two of the game's biggest 
						disasters at Heysel and Hillsborough. Both the good and 
						bad have shaped the identity of a fiercely proud club.<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;">
						</p>
						Liverpool were formed in 1892 because of a dispute 
						between Everton FC and John Houlding, who owned the 
						Anfield ground on which Everton played. Houlding founded 
						a new club, Everton Athletic, whose name soon changed to 
						Liverpool.<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;">
						</p>
						The club were admitted to the Football League in 1893 
						and started as they meant to go on: Liverpool won 
						Division Two and remained unbeaten throughout the leagie 
						season. They won the Division One title for the first 
						time in 1901 and were also champions in 1906, 1922, 1923 
						and 1947, yet it was not until the Sixties that they 
						began to establish themselves as a world power.<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;">
						</p>
						The catalyst was the appointment of Bill Shankly in 
						1959. At the time Liverpool were in Division Two, and 
						had just lost to non-league Worcester City in the FA 
						Cup. Shankly ripped the club up and started again: he 
						released 24 players, and introduced the legendary "Boot 
						Room", where coaches would sit and talk tactics.<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;">
						</p>
						Liverpool were promoted as champions in 1962, and won 
						Division One two years later. It was the beginning of a 
						relentless stream of trophies over the next 30 years. 
						Shankly's side won their first FA Cup in 1965, and added 
						league titles in 1966 and 1973 - when they also won 
						their first European trophy, the UEFA Cup.<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;">
						</p>
						After another FA Cup win in 1974, Shankly was replaced 
						by his assistant Bob Paisley; he was even more prolific. 
						In 10 seasons under Paisley, Liverpool won three 
						European Cups, six league championships, a Uefa Cup and 
						three League Cups. This was success on a mind-blowing 
						scale. Notable highlights included their first European 
						Cup, achieved with a 3-1 win over Borussia 
						Monchengladbach in Rome in 1977; a remarkable 1978-79 
						league campaign, in which Liverpool won 30 of their 42 
						games and conceded only 16 goals; and victory over Real 
						Madrid in the 1981 European Cup final, a decent 
						consolation for the only season between 1972 and 1992 in 
						which Liverpool finished outside the top two.<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;">
						</p>
						Paisley retired in 1983 after three consecutive league 
						championships - the first of which came when they were 
						12th at Christmas - and his successor Joe Fagan made it 
						three in a row in his first season, when he also won the 
						League Cup (Liverpool's fourth in a row) and the 
						European Cup after a famous penalty shoot-out against 
						Roma in Rome.<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;">
						</p>
						The 1984-85 season was Liverpool's first trophyless 
						campaign for a deacde, but it was not remembered for 
						that. Before the European Cup final against Juventus in 
						Heysel, 39 fans, mostly Italian, were killed after crowd 
						disorder. As a result, English clubs were banned for 
						Europe for five years. Fagan was replaced by Kenny 
						Dalglish as player-manager in that summer, and he won 
						the club's first Double in his first season. Liverpool 
						might have won three more consecutive Doubles from 1988 
						to 1990, when instead they had to settle for two titles 
						and an FA Cup. The most famous example came in 1989 
						when, having already won the FA Cup, they lost the title 
						to Arsenal when Michael Thomas scored a last-minute goal 
						at Anfield in the final match of the season.<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;">
						</p>
						Yet that period was overshadowed by another tragedy, 
						this time at Hillsborough in 1989, when 96 Liverpool 
						fans died during the FA Cup semi-final against 
						Nottingham Forest. It took a huge toll on Dalglish, who 
						eventually resigned unexpectedly almost two years later. 
						Liverpool were reigning champions at the time but have 
						not won a title since, challenging only occasionally.<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;">
						</p>
						The Nineties were largely barren, with only an FA Cup in 
						1992 and a League Cup in 1995. The appointment of Gerard 
						Houllier in 1998 brought sobriety and order, and in his 
						third season he achieved a treble of the UEFA Cup, FA 
						Cup and League Cup. But Houllier struggled to build on 
						that, even though he did win another League Cup in 2003, 
						and eventually left the club in the summer of 2004.<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;">
						</p>
						His successor, Rafael Benitez, was an instant hit: 
						despite finishing fifth in the league in his first 
						season, Liverpool won the Champions League in 
						astonishing circumstances. They were 3-0 down to Milan 
						at half-time in the final, but scored three goals in six 
						minutes before winning on penalties. It was quickly 
						christened "The Miracle of Istanbul".<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;">
						</p>
						Benitez won the FA Cup in 2006, and Liverpool lost 
						another Champions League final against Milan a year 
						later, but, despite going closest in 2008-9, he largely 
						struggled in the league and left the club in 2010 after 
						a disastrous campaign. By then, Liverpool's biggest 
						problems were off the field: the club had been taken 
						over by two Americans, George Gillett and Tom Hicks, in 
						2007, whose reign the majority of fans were extremely 
						unhappy with. When they were finally forced out in the 
						winter of 2010, Liverpool could look to the future with 
						a greater degree of confidence under the eventual 
						leadership of the legendary Dalglish again.<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;">
						</p>
						<p style="margin: 36px 0px; line-height: 30px; font-style: normal; font-variant: normal; font-weight: normal; font-stretch: normal; font-size: 18px; font-family: BentonSans, Helvetica, Arial, sans-serif;">
						Look they did, but results were not forthcoming. 
						Liverpool spent big and won the Carling Cup while 
						reaching the FA Cup final. But they finished in 8th in 
						the Premier League and Dalglish left by mutual consent 
						with Swansea's Brendan Rodgers hoping to bring his 
						attacking, passing style to Anfield.</p>
